function mask_border = nyuv2_find_border(images)
% Try to find a mask indicating all pixels belonging to the border of one
% of the images. This was used to determine how many pixels to crop at each
% side.
%
% INPUT:
% images:       matrix containing alls images along the fourth dimension
%
% OUTPUT:
% mask_border:  mask indicating border pixels
%

    [height, width, ~, frames] = size(images);

    mask_border = ones(height,width);
    for i = 1: frames
      image = rgb2gray(images(:, :, :, i));
      mask_border = (mask_border & (image >= 253));
    end;
end